	LcdWriteCom(0x28);	 //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฮป๏ฟฝ๏ฟฝ๏ฟฝยตฤณ๏ฟฝสผ๏ฟฝ๏ฟฝ
	LcdWriteCom(0x0c);  //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝสพ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝสพ๏ฟฝ๏ฟฝ๏ฟฝ
	LcdWriteCom(0x06);  //ะดาป๏ฟฝ๏ฟฝึธ๏ฟฝ๏ฟฝ๏ฟฝ1
	LcdWriteCom(0x01);  //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
