#include"lcd.h"

/*******************************************************************************
* ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ         : Lcd1602_Delay1ms
* ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ		   : ๏ฟฝ๏ฟฝสฑ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝสฑ1ms
* ๏ฟฝ๏ฟฝ    ๏ฟฝ๏ฟฝ         : c
* ๏ฟฝ๏ฟฝ    ๏ฟฝ๏ฟฝ         : ๏ฟฝ๏ฟฝ
* หต    ๏ฟฝ๏ฟฝ         : ๏ฟฝรบ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ12MHZ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝยฃ๏ฟฝ12๏ฟฝ๏ฟฝฦต๏ฟฝ๏ฟฝฦฌ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝสฑ๏ฟฝ๏ฟฝ
*******************************************************************************/

void Lcd1602_Delay1ms(uint c)   //๏ฟฝ๏ฟฝ๏ฟฝ 0us
{
    uchar a,b;
	for (; c>0; c--)
	{
		 for (b=199;b>0;b--)
		 {
		  	for(a=1;a>0;a--);
		 }      
	}
    	
}

/*******************************************************************************
* ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ         : LcdWriteCom
* ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ		   : ๏ฟฝ๏ฟฝLCDะด๏ฟฝ๏ฟฝาป๏ฟฝ๏ฟฝ๏ฟฝึฝฺต๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
* ๏ฟฝ๏ฟฝ    ๏ฟฝ๏ฟฝ         : com
* ๏ฟฝ๏ฟฝ    ๏ฟฝ๏ฟฝ         : ๏ฟฝ๏ฟฝ
*******************************************************************************/
#ifndef 	LCD1602_4PINS	 //๏ฟฝ๏ฟฝรป๏ฟฝะถ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝLCD1602_4PINSสฑ
void LcdWriteCom(uchar com)	  //ะด๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
{
	LCD1602_E = 0;     //สน๏ฟฝ๏ฟฝ
	LCD1602_RS = 0;	   //ัก๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
	LCD1602_RW = 0;	   //ัก๏ฟฝ๏ฟฝะด๏ฟฝ๏ฟฝ
	
	LCD1602_DATAPINS = com;     //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
	Lcd1602_Delay1ms(1);		//๏ฟฝศด๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝศถ๏ฟฝ

	LCD1602_E = 1;	          //ะด๏ฟฝ๏ฟฝสฑ๏ฟฝ๏ฟฝ
	Lcd1602_Delay1ms(5);	  //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝสฑ๏ฟฝ๏ฟฝ
	LCD1602_E = 0;
}
#else 
void LcdWriteCom(uchar com)
{
	LCD1602_E = 0;
	LCD1602_RS = 0;
	LCD1602_RW = 0;

	LCD1602_DATAPINS = com;
	Lcd1602_Delay1ms(1);

	LCD1602_E = 1;
	Lcd1602_Delay1ms(1);
	LCD1602_E = 0;

	Lcd1602_Delay1ms(1);
	LCD1602_DATAPINS = com << 4;
	Lcd1602_Delay1ms(1);

	LCD1602_E = 1;
	Lcd1602_Delay1ms(1);
	LCD1602_E = 0;
}
#endif
/*******************************************************************************
* ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ         : LcdWriteData
* ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ		   : ๏ฟฝ๏ฟฝLCDะด๏ฟฝ๏ฟฝาป๏ฟฝ๏ฟฝ๏ฟฝึฝฺต๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
* ๏ฟฝ๏ฟฝ    ๏ฟฝ๏ฟฝ         : dat
* ๏ฟฝ๏ฟฝ    ๏ฟฝ๏ฟฝ         : ๏ฟฝ๏ฟฝ
*******************************************************************************/		   
#ifndef 	LCD1602_4PINS		   
void LcdWriteData(uchar dat)			//ะด๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
{
	LCD1602_E = 0;	//สน๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
	LCD1602_RS = 1;	//ัก๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
	LCD1602_RW = 0;	//ัก๏ฟฝ๏ฟฝะด๏ฟฝ๏ฟฝ

	LCD1602_DATAPINS = dat; //ะด๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
	Lcd1602_Delay1ms(1);

	LCD1602_E = 1;   //ะด๏ฟฝ๏ฟฝสฑ๏ฟฝ๏ฟฝ
	Lcd1602_Delay1ms(5);   //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝสฑ๏ฟฝ๏ฟฝ
	LCD1602_E = 0;
}
#else
void LcdWriteData(uchar dat)
{
	LCD1602_E = 0;
	LCD1602_RS = 1;
	LCD1602_RW = 0;

	LCD1602_DATAPINS = dat;
	Lcd1602_Delay1ms(1);

	LCD1602_E = 1;
	Lcd1602_Delay1ms(1);
	LCD1602_E = 0;

	Lcd1602_Delay1ms(1);
	LCD1602_DATAPINS = dat << 4;
	Lcd1602_Delay1ms(1);

	LCD1602_E = 1;
	Lcd1602_Delay1ms(1);
	LCD1602_E = 0;
}
#endif
